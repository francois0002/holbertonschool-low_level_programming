/**
* flip_bits-  sets the value of a bit to 0 at a given index.
* @n :numbers
* @m :numbers
* Return:the value of the bit at index
*/

unsigned int flip_bits(unsigned long int n, unsigned long int m)
{
	unsigned int result;

	n = 0;
	m = 0;


	result = n +  m;
	return (result);
}
